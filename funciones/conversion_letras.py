def escribir_palabra(palabra):
    palabra = palabra.lower()
    lineas = ['' for _ in range(6)]  

    for letra in palabra:
        if letra in abecedario:
            for i in range(6):  
                lineas[i] += abecedario[letra][i]  

        else:
            for i in range(6):
                lineas[i] += ' ' * 6  

    for linea in lineas:
        print(linea)  

